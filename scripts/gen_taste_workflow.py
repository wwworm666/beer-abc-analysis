"""Генератор workflow-скрипта переписывания дескрипторов вкуса (3 слова на сорт)
по правилам владельца (честность состава, без фантомных фруктов, лагер=чистый,
порядок=акцент). Читает data/menu_cards.json, зашивает компактные карточки + правила
+ эталоны + override-ответы владельца, пишет scripts/taste_workflow.js.

Запуск: py -3 scripts/gen_taste_workflow.py
Затем main-loop вызывает Workflow со scriptPath=scripts/taste_workflow.js.
"""
import json, pathlib

ROOT = pathlib.Path(__file__).resolve().parent.parent
cards = json.load(open(ROOT / "data/menu_cards.json", encoding="utf-8"))

# Ответы владельца на 6 сортов-примеров — проставляем напрямую, агентам не отдаём.
OVERRIDES = {
    102: ["карамельный", "бисквитный", "ликёрный"],
    80:  ["винный", "бархатистый", "кисло-сладкий"],
    5:   ["чистый", "хлебный", "питкий"],
    101: ["чистый", "солодовый", "мягкий"],
    118: ["кофейный", "шоколадный", "жжёный"],
    116: ["горький", "сухой", "цитрусовый"],
}

RULES = """ПРАВИЛА ДЕСКРИПТОРОВ ВКУСА (соблюдай строго):
1. Ровно ТРИ отдельных СЛОВА-дескриптора, НЕ фразы. («сухое послевкусие» — запрещено.)
2. Честность состава. Дескриптор опирается на то, что РЕАЛЬНО в пиве (солод, хмель,
   дрожжи, процесс), а не на ассоциацию, намекающую на ОТСУТСТВУЮЩИЙ ингредиент.
   Сливы в квадрюпеле — нельзя. Банан в пшеничке — НЕЛЬЗЯ (дрожжевой эфир, названный
   фруктом = фантомный фрукт).
3. Названия солодов/процессов — МОЖНО. карамельный, шоколадный, бисквитный, жжёный
   честны: это сами солода (карамельный/шоколадный/бисквитный/жжёный солод), а не
   выдуманный фрукт.
4. Исключение для ХМЕЛЯ. Сортовой характер хмеля можно звать его фруктовым
   дескриптором (цитрусовый для Citra, тропический для современных хмелей) — это
   собственный характер хмеля, а не добавленный в пиво фрукт. Решай под каждый случай.
5. Не пиши то, что на этикетке звучит как БРАК, даже если технически верно:
   «спиртовой» -> ликёрный/согревающий; «смолистый» убрать. Но смотри по сорту:
   «горький» для IPA и «жжёный» для стаута — это ЖЕЛАННЫЙ характер, а не дефект;
   одинокий «кислый» читается как «прокисло» -> «кисло-сладкий».
6. Никакой пустой маркетинговой воды. Слова-впечатления без вкусовой инфы («яркий») —
   убрать. При этом «чистый / питкий / освежающий / мягкий» — ОК, несут конкретику.
7. Не переобещай интенсивность. «яркий» на мягком пиве врёт и работает против продажи.
8. Без дублей — три РАЗНЫЕ оси. Не трать два слота на одно: «горький+хмелевой» = одно
   и то же; «херес+ликёр» — оба про алкоголь. Лучше солод / финиш / хмель и т.п.
9. Лагер — акцент на ЧИСТОТЕ, а не на параде вкусов. Веди «чистый», максимум один
   лёгкий вкусовой якорь.
10. Плотность вкусов — под стиль. У стаута наоборот: кофе/шоколад/жжёный и есть
    идентичность, несколько вкусовых слов уместны.
11. Порядок = АКЦЕНТ. Главное слово вперёд (винный, чистый)."""

EXAMPLES = """ЭТАЛОНЫ (под эти ответы откалибруйся):
- Gulden Draak 708 (тёмный трипель 10,5%) -> карамельный, бисквитный, ликёрный
- Bourgogne des Flandres (фландрский эль 5,0%) -> винный, бархатистый, кисло-сладкий
- Ferdinand 10 (чешский лагер 4%) -> чистый, хлебный, питкий
- ФестХаус Хеллес (хеллес 5,2%) -> чистый, солодовый, мягкий
- Брюлок Стаут (овсяный стаут 4,5%) -> кофейный, шоколадный, жжёный
  (если молочный -> жжёный меняем на кремовый; если имперский -> добавляем ликёрный/хересный)
- Островица Холи ДИПА (ДИПА 8,2%) -> горький, сухой, цитрусовый"""

# Компактные карточки для агентов (без override-сортов).
agent_cards = []
for c in cards:
    if c["id"] in OVERRIDES:
        continue
    agent_cards.append({
        "id": c["id"],
        "name": c.get("name", ""),
        "latin": c.get("latin", ""),
        "brewery": c.get("brewery", ""),
        "country": c.get("country", ""),
        "style": c.get("style", ""),
        "abv": c.get("abv", ""),
        "cur": c.get("tags", []),
    })

over_list = [{"id": k, "tags": v} for k, v in OVERRIDES.items()]

js = """export const meta = {
  name: 'menu-taste-descriptors',
  description: 'Переписать 3 честных дескриптора вкуса на каждую карточку пива по правилам владельца',
  phases: [{ title: 'Descriptors', detail: 'один агент на сорт' }],
}

const RULES = __RULES__;
const EXAMPLES = __EXAMPLES__;
const CARDS = __CARDS__;
const OVERRIDES = __OVERRIDES__;

const SCHEMA = {
  type: 'object',
  additionalProperties: false,
  properties: {
    id: { type: 'integer' },
    tags: { type: 'array', items: { type: 'string' }, minItems: 3, maxItems: 3,
            description: 'ровно 3 отдельных слова-дескриптора, главное вперёд' },
    rationale: { type: 'array', items: { type: 'string' }, minItems: 3, maxItems: 3,
                 description: 'по короткой причине на каждое слово (на что в составе опирается)' },
    needs_review: { type: 'boolean',
                    description: 'true если решал спорный случай: характер хмеля, дрожжевой эфир, слово про алкоголь, либо не уверен в составе' },
    note: { type: 'string', description: 'короткая заметка о спорных решениях (можно пусто)' },
  },
  required: ['id', 'tags', 'rationale', 'needs_review'],
}

function promptFor(c) {
  return [
    'Ты пишешь дескрипторы вкуса для карточки пивного меню. Соблюдай правила владельца ДОСЛОВНО.',
    '',
    RULES,
    '',
    EXAMPLES,
    '',
    'СОРТ:',
    'name (RU): ' + c.name,
    'latin/оригинал: ' + (c.latin || '—'),
    'пивоварня: ' + (c.brewery || '—'),
    'страна: ' + (c.country || '—'),
    'стиль: ' + (c.style || '—'),
    'ABV: ' + (c.abv || '—') + '%',
    'текущие (плохие) дескрипторы: ' + ((c.cur || []).join(', ') || '—'),
    '',
    'ЗАДАЧА: дай РОВНО 3 отдельных слова-дескриптора по правилам, главное слово первым.',
    'Опирайся на реальный состав/характер стиля этого пива (солод, хмель, дрожжи, процесс).',
    'Если это лагер — веди «чистый», максимум один вкусовой якорь. Если стаут/портер —',
    'плотный набор кофе/шоколад/жжёный/ореховый уместен. Не повторяй одну ось дважды.',
    'Не выдумывай фрукты, которых в пиве нет (фантомный фрукт). Характер ХМЕЛЯ фруктом',
    'звать можно. Слова, звучащие как брак, замени (спиртовой->ликёрный, убери смолистый).',
    'Если не уверен в точном составе конкретной марки — можешь кратко поискать в вебе',
    '(untappd / сайт пивоварни / profibeer) через ToolSearch->WebSearch; иначе опирайся',
    'на канон стиля и будь консервативен (солод/процесс/чистота вместо выдуманной',
    'конкретики). Верни строго JSON по схеме (StructuredOutput).',
  ].join('\\n');
}

phase('Descriptors')
log('Переписываю дескрипторы для ' + CARDS.length + ' сортов (+' + OVERRIDES.length + ' заданы владельцем напрямую)')

const research = await pipeline(
  CARDS,
  c => agent(promptFor(c), { label: 'tags:' + c.id, phase: 'Descriptors', schema: SCHEMA })
        .then(r => r ? { ...r, id: c.id } : null)
)

const out = research.filter(Boolean)
for (const o of OVERRIDES) {
  out.push({ id: o.id, tags: o.tags, rationale: ['эталон владельца','эталон владельца','эталон владельца'], needs_review: false, note: 'override владельца' })
}
log('Готово: ' + out.length + ' карточек')
return out
"""
js = (js
      .replace("__RULES__", json.dumps(RULES, ensure_ascii=False))
      .replace("__EXAMPLES__", json.dumps(EXAMPLES, ensure_ascii=False))
      .replace("__CARDS__", json.dumps(agent_cards, ensure_ascii=False))
      .replace("__OVERRIDES__", json.dumps(over_list, ensure_ascii=False)))

out_path = ROOT / "scripts/taste_workflow.js"
out_path.write_text(js, encoding="utf-8")
print(f"agents (non-override): {len(agent_cards)}")
print(f"overrides: {len(over_list)}")
print(f"written: {out_path}")
